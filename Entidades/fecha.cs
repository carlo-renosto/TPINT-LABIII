
namespace Entidades {
    public class fecha {
        private int dia;
        private int mes;
        private int anio;

        public fecha() {

        }

        public int getdia() {
            return dia;
        }

        public int getmes() {
            return mes;
        }

        public int getanio() {
            return anio;
        }

        public void setdia(int d) {
            dia = d;
        }

        public void setmes(int m) {
            mes = m;
        }

        public void setanio(int a) {
            anio = a;
        }
    }
}
